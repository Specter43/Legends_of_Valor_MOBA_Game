/**
 * A Class for storing color strings.
 */
public class Printer {
    public final String RESET = "\033[0m";
    public final String RED = "\033[91;1m";
    public final String GREEN = "\033[92;1m";
    public final String YELLOW = "\033[93;1m";
    public final String BLUE = "\033[94;1m";
    public final String CYAN = "\033[96;1m";
    public final String RED_BG = "\u001B[41m";
    public final String WHITE_BG = "\u001B[47m";

    public final String BLUE_BG = "\u001B[44m";
}
